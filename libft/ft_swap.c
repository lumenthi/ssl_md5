#include "libft.h"

uint16_t	swap_uint16(uint16_t nb)
{
	nb = (nb << 8) | (nb >> 8);
	return (nb);
}

uint32_t	swap_uint32(uint32_t nb)
{
	nb = ((nb & 0x000000FF) << 24 |
				(nb & 0x0000FF00) << 8 |
				(nb & 0x00FF0000) >> 8 |
				(nb & 0xFF000000) >> 24);
	return (nb);
}

uint64_t	swap_uint64(uint64_t nb)
{
	nb = (
		(((nb >> 56) & 0x00000000000000FF)) |
		(((nb >> 40) & 0x000000000000FF00)) |
		(((nb >> 24) & 0x0000000000FF0000)) |
		(((nb >> 8)  & 0x00000000FF000000)) |
		(((nb << 56) & 0xFF00000000000000)) |
		(((nb << 40) & 0x00FF000000000000)) |
		(((nb << 24) & 0x0000FF0000000000)) |
		(((nb << 8)  & 0x000000FF00000000))
	);
	return (nb);
}
